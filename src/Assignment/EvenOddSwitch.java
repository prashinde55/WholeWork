package Assignment;

public class EvenOddSwitch {

	public static void main(String[] args) {
		
		int num=3;
		switch (num)
		{
		case 2:
		case 4:
		case 6:
		case 8:
		case 10:
		case 12:
		case 14:
			System.out.println(" this is even num");
			break;
			default:
			System.out.println("this is odd num");
			
		
		
		}

	}

}
